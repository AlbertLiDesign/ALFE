        public List<Vector3D> ExtractTopOptIsosurface(Model model, List<double> vertSensitivities, double isovalue, Vector3D voxelSize, bool interpolation)
        {
            List<Vector3D> allVerts = new List<Vector3D>();
            List<Vector3D> voxelVerts = new List<Vector3D>(model.Nodes.Count);
            List<int[]> voxels = new List<int[]>(model.Elements.Count);

            foreach (var item in model.Nodes)
                voxelVerts.Add(new Vector3D(item.Position.X, item.Position.Y, item.Position.Z));

            foreach (var item in model.Elements)
            {
                int[] vertsIdx = new int[8];
                vertsIdx[0] = item.Nodes[1].ID;
                vertsIdx[1] = item.Nodes[0].ID;
                vertsIdx[2] = item.Nodes[3].ID;
                vertsIdx[3] = item.Nodes[2].ID;
                vertsIdx[4] = item.Nodes[5].ID;
                vertsIdx[5] = item.Nodes[4].ID;
                vertsIdx[6] = item.Nodes[6].ID;
                vertsIdx[7] = item.Nodes[7].ID;
                voxels.Add(vertsIdx);
            }


            for (int i = 0; i < model.Elements.Count; i++)
            {
                Vector3D[] voxelCorners = new Vector3D[8];
                double[] localSensitivities = new double[8];
                for (int j = 0; j < 8; j++)
                {
                    voxelCorners[j] = voxelVerts[voxels[i][j]];
                    localSensitivities[j] = vertSensitivities[voxels[i][j]];
                }

                var verts = ExtractIsosurface(voxelCorners, localSensitivities, isovalue, voxelSize, interpolation);
                foreach (var item in verts)
                    allVerts.Add(item);
            }
            return allVerts;
        }
